package com.wuzhi.index.bean;

public class Personal {
    private String id;
    private String name;
    private String type;
    private String hope_money;
    private String address;
    private String exp;
    private String edu;
    private String university;
    private String rongyu;
    private String city;

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getHope_money() {
        return hope_money;
    }

    public void setHope_money(String hope_money) {
        this.hope_money = hope_money;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getExp() {
        return exp;
    }

    public void setExp(String exp) {
        this.exp = exp;
    }

    public String getEdu() {
        return edu;
    }

    public void setEdu(String edu) {
        this.edu = edu;
    }

    public String getUniversity() {
        return university;
    }

    public void setUniversity(String university) {
        this.university = university;
    }

    public String getRongyu() {
        return rongyu;
    }

    public void setRongyu(String rongyu) {
        this.rongyu = rongyu;
    }
}
