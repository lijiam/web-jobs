package com.wuzhi.index.bean;

public class Company {

    private String id;
    private String name;
    private String type;
    private String rongzi;
    private String guimo;
    private String hot_job;
    private String logo;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getRongzi() {
        return rongzi;
    }

    public void setRongzi(String rongzi) {
        this.rongzi = rongzi;
    }

    public String getGuimo() {
        return guimo;
    }

    public void setGuimo(String guimo) {
        this.guimo = guimo;
    }

    public String getHot_job() {
        return hot_job;
    }

    public void setHot_job(String hot_job) {
        this.hot_job = hot_job;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }
}
